/**********************************************************************
 * @brief  Longest line length among the given lines.
 **********************************************************************/
static size_t longest_line(const char * const * pp_lines,
                           int line_count)
{
    size_t longest = 0;
    int    i       = 0;

    for (i = 0; i < line_count; i++)
    {
        size_t len = (NULL != pp_lines[i]) ? strlen(pp_lines[i]) : 0U;

        if (len > longest)
        {
            longest = len;
        }
    }

    return longest;
}

/**********************************************************************
 * @brief  Blit one glyph into the RGBA buffer at (px, py).
 **********************************************************************/
static void blit_glyph(uint8_t * p_img, uint32_t img_w, char ch,
                       int px, int py, int scale)
{
    int idx = (int)((unsigned char)ch) - FIRST_GLYPH;
    int row = 0;
    int col = 0;
    int sx  = 0;
    int sy  = 0;

    if ((0 > idx) || (idx >= GLYPH_COUNT))
    {
        goto cleanup;   /* Non-printable: leave blank. */
    }

    for (row = 0; row < GLYPH_H; row++)
    {
        uint8_t bits = g_font[idx][row];

        for (col = 0; col < GLYPH_W; col++)
        {
            if (0U == ((bits >> col) & 1U))
            {
                continue;
            }

            /* Scale the set pixel into a scale x scale block. */
            for (sy = 0; sy < scale; sy++)
            {
                for (sx = 0; sx < scale; sx++)
                {
                    uint32_t x = (uint32_t)(px + (col * scale) + sx);
                    uint32_t y = (uint32_t)(py + (row * scale) + sy);
                    size_t   o = (((size_t)y * img_w) + x) * 4U;

                    p_img[o + 0U] = 255U;
                    p_img[o + 1U] = 255U;
                    p_img[o + 2U] = 255U;
                    p_img[o + 3U] = 255U;
                }
            }
        }
    }

cleanup:
    return;
}

/**********************************************************************
 * @brief  Rasterise text lines into a newly allocated RGBA buffer.
 **********************************************************************/
uint8_t * hud_render_lines(const char * const * pp_lines,
                           int line_count, int scale,
                           uint32_t * p_width, uint32_t * p_height)
{
    uint8_t * p_img   = NULL;
    uint8_t * p_out   = NULL;
    size_t    cols    = 0;
    uint32_t  width   = 0;
    uint32_t  height  = 0;
    size_t    bytes   = 0;
    int       line    = 0;

    if ((NULL == pp_lines) || (0 >= line_count) || (1 > scale) ||
        (NULL == p_width) || (NULL == p_height))
    {
        goto cleanup;
    }

    cols   = longest_line(pp_lines, line_count);
    if (0U == cols)
    {
        goto cleanup;
    }

    width  = (uint32_t)(cols * (size_t)(GLYPH_W * scale));
    height = (uint32_t)((size_t)line_count
                        * (size_t)((GLYPH_H + LINE_GAP_PX) * scale));

    bytes  = (size_t)width * (size_t)height * 4U;
    p_img  = (uint8_t *)calloc(bytes, sizeof(uint8_t));
    if (NULL == p_img)
    {
        goto cleanup;
    }

    for (line = 0; line < line_count; line++)
    {
        const char * p_line = pp_lines[line];
        int          py     = line * (GLYPH_H + LINE_GAP_PX) * scale;
        int          c      = 0;

        if (NULL == p_line)
        {
            continue;
        }

        for (c = 0; '\0' != p_line[c]; c++)
        {
            blit_glyph(p_img, width, p_line[c], c * GLYPH_W * scale, py,
                       scale);
        }
    }

    *p_width  = width;
    *p_height = height;
    p_out     = p_img;
    p_img     = NULL;

cleanup:
    if (NULL != p_img)
    {
        free(p_img);
    }
    return p_out;
}
